package by.epam.safonenko.pharmacy.entity;

public class Card {
    private String number;
    private String code;

    public Card(){
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

}
